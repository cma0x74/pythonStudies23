# calcula seno, cosseno e tangente de qualquer ângulo

import math
a = eval(input('fornece um ângulo qualquer '))

seno = math.sin(a)
cosseno = math.cos(a)
tangente = math.tan(a)

print(f'seno de {a} = {seno}')
print(f'cosseno = {cosseno}')
print(f'tangente = {tangente}')

